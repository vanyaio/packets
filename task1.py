def vec_mult(p1, p2):
    return p1[0] * p2[1] - p1[1] * p2[0]

def square(points):
    x = points[0]
    y = points[1]

    s = 0
    for i in range(len(x) - 1):
        s += vec_mult((x[i], y[i]), (x[i + 1], y[i + 1]))
    s += vec_mult((x[-1], y[-1]), (x[0], y[0]))

    return abs(s) / 2

def get_k(mid, x1, x2):
    vx = x2 - x1
    vx = float(vx)
    return (mid - x1) / vx

def slice(j1, j2, mid, x, u):
    k1 = get_k(mid, x[j1], x[j1 + 1])
    mid_y1 = u[j1] + k1 * (u[j1 + 1] - u[j1])

    k2 = get_k(mid, x[j2], x[j2 + 1])
    mid_y2 = u[j2] + k1 * (u[j2 + 1] - u[j2])

    new_x = x[j1 + 1 :  j2 + 1]
    new_x.insert(0, mid)
    new_x.append(mid)

    new_y = u[j1 + 1 :  j2 + 1]
    new_y.insert(0, mid_y1)
    new_y.append(mid_y2)

    return (new_x, new_y) 

def diff(mid, i_start, i_end, x, u):
    j1 = 0
    j2 = 0
    j3 = 0

    for i in range(len(x)-1):
        if ((x[i] <= mid) and (mid <= x[i + 1])):
            j1 = i
            break
    
    for i in range(i_end, i_start):
        if ((x[i] >= mid) and (mid >= x[i + 1])):
            j2 = i
            break

    for i in range(i_start, len(x)-1):
        if ((x[i] <= mid) and (mid <= x[i + 1])):
            j3 = i
            break
    
    s_right = square(slice(j1, j2, mid, x, u))
    s_left = square(slice(j2, j3, mid, x, u))

    return s_left - s_right

